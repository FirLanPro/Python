# текст, редактирование
text = 'AbcdeiFghijkLmnopqrsTuvwxyz'
print(len(text)) # количество символов в text
print('xyz' in text) # есть xyz в text
print(text.lower()) # перевод всех букв в нижний регистр
print(text.upper()) # перевод всех букв в верхний регистр
print(text.replace('xyz', ' икс игрик зет')) # xyz в text поменять на икс игрик зет