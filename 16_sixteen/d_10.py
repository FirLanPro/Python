# функция enumerate()
# применяется к итерируемому объекту 
# и возвращает новый итератор с кортежами 
# из индекса и элементов входных данных

list_1 = list(enumerate(['казань', 'смоленск', 'Рыбки', 'чикаго']))
print(list_1)

#
user = ['3434', '565656','454545']
list_2 = list(enumerate(user))
print(list_2)