# словари. изменяемые элементы и значения. не индексируемые. уникальные элементы и ключи. {}. dict()
#  добавление в словарь.

d = {} # создан пустой словарь
d = dict() # пустой словарь 

d['q'] = 'qwerty' # ключ q, возвращает qwerty
print(d)
d['w'] = 'werty' # ключ w, возвращает wwerty
print(d)

print(d['q']) # вывод qwerty
